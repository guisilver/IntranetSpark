package br.com.oma.intranet.util;

public class FuncionarioHorario {

	private String entr_prim;
	private String saida_prim;
	private String entr_seg;
	private String saida_seg;

	public String getEntr_prim() {
		return entr_prim;
	}

	public void setEntr_prim(String entr_prim) {
		this.entr_prim = entr_prim;
	}

	public String getSaida_prim() {
		return saida_prim;
	}

	public void setSaida_prim(String saida_prim) {
		this.saida_prim = saida_prim;
	}

	public String getEntr_seg() {
		return entr_seg;
	}

	public void setEntr_seg(String entr_seg) {
		this.entr_seg = entr_seg;
	}

	public String getSaida_seg() {
		return saida_seg;
	}

	public void setSaida_seg(String saida_seg) {
		this.saida_seg = saida_seg;
	}

}